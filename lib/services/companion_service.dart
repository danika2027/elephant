  /// 生成今日陪伴消息
  /// [daysSinceArrival] 到达后第几天（1=到达当天）
  ElephantMessage generateMessage({
    required int daysSinceArrival,
    required ElephantProfile profile,
  }) {
    // 确定性选择：同一天看到同一条，不同天看到不同条
    final idx = (daysSinceArrival - 1) * 7 % _pool.length;
    final data = _pool[idx];

    return ElephantMessage(
      type: 'companion',
      timeOfDay: data['timeOfDay']!,
      location: data['location']!,
      content: data['content']!.replaceAll('{name}', profile.name),
      signature: '——你的${profile.name}',
    );
  }

  /// 旅程统计数据
  Map<String, String> getJourneyStats({
    required List<DayData> days,
    required JourneyMeta meta,
  }) {
    final totalKm = meta.totalDistanceKm.toInt();
    final totalDays = days.length;
    // 收集所有地名
    final places = <String>{};
    for (final d in days) {
      places.add(d.destination);
      places.add(d.lodging.split('·').first.trim());
    }

    return {
      'totalDays': '$totalDays 天',
      'totalKm': '$totalKm km',
      'places': '${places.length} 个',
    };
  }
